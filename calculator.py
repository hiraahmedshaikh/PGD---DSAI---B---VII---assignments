# for addition
def addition (x,y):
    return x+y

# for substraction
def substraction (x,y):
    return x-y

# for multiplication
def multiply(a, b):
    return a * b

# for remainder
def remainder(a, b):
    return a % b

# for cube
def cube(a):
    return a ** 3

# for square
def square(a):
    return a ** 2

